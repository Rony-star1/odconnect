export function getDistrictInOdia(englishName: string): string {
  const district = ODISHA_DISTRICTS.find(d => d.name === englishName);
  return district?.nameOdia || englishName;
}

export function getInterestInOdia(englishName: string): string {
  const cultural = CULTURAL_INTERESTS.find(i => i.name === englishName);
  if (cultural) return cultural.nameOdia;
  
  const common = COMMON_INTERESTS.find(i => i.name === englishName);
  return common?.nameOdia || englishName;
}